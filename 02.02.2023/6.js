"use strict"

const euro = 0.92;

let usd = +prompt(`Скільки ваших USD будемо конвертувати в EUR?`)
let r = usd / euro;
alert (`Ось, Ви отримали ${r} EUR`);

r = 0;
r = Math.floor(usd, euro);
alert (`А за цю послугу, копійки ми собі візьмемо, тому ось тримайте ваші ${r} EUR :)`);
